# [152] Maximum Product Subarray
#

# @lc code=start
class Solution:
    def maxProduct(self, nums: list[int]) -> int:
        A, B = nums, nums[::-1]
        for i in range(1, len(A)):
            A[i] *= A[i - 1] or 1
            B[i] *= B[i - 1] or 1

        return max(A + B)


